package myAssignments.day1;

public class SwitchCase {
	
	public static void main (String args[]) {
		
	 char city = 'C';
		
	  switch (city) {
		
		case 'C':
			
			System.out.println("Chennai city");
			break;
			
		case 'H':
			
			System.out.println("Hyderabad city");
			break;
			
		case 'B':
			
			System.out.println("Bangalore city");
			break;
			
		case 'M':
			
			System.out.println("Mumbai city");
			break;
			
		case 'D':
			
			System.out.println("Delhi city");
			break;
		
		}
		
	}

}
